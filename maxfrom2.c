// In this we will know - How to use If-Else statement in C

#include<stdio.h>
void main()
{
    int a , b;
    printf("Enter the value of a");
    scanf("%d",&a);
    printf("Enter the value of b");
    scanf("%d",&b);
    if(a>b)
    {
        printf("%d is Greater",a);
    }
    else
    {
        printf("%d is greater",b);
    }
}
